static int mnist_input_idx = 0;

static int read_mnist_input(void* dst, const size_t size, const size_t n) {
    if (mnist_input_idx+size*n <= sizeof(MNIST_INPUT_IMAGE)) {
        if (dst){
            memcpy(dst, (void*)(&MNIST_INPUT_IMAGE[mnist_input_idx]), size*n);
            mnist_input_idx += size*n;
        } else {
            // if pointer is empty, we do not need to read, just increase the idx
            mnist_input_idx += size*n;
        }
        return n;
    } else {
        printf("%s out of bounds %lu > %lu\n", __func__, mnist_input_idx+size*n, sizeof(MNIST_INPUT_IMAGE));
        return -1;
    }
}

static int feof_mnist_input() {
    return  mnist_input_idx == sizeof(MNIST_INPUT_IMAGE) ? 1 : 0;
}

void envRead(unsigned int size,
             unsigned int channelsHeight,
             unsigned int channelsWidth,
             DATA_T* data,
             unsigned int outputsSize,
             Target_T* outputTargets)
{
    char header[2];
    read_mnist_input(header, sizeof(header[0]), 2);

    if (header[0] != 'P' || (header[1] != '5' && header[1] != '6')) {
        printf("Unknown PGM file format\n");
        return;
    }

    int pixelWidth;
    int pixelHeight;
    int maxValue;
    pixelWidth = 24;
    pixelHeight = 24;
    maxValue = 255;
    read_mnist_input(NULL, sizeof(char), 11);

    if (pixelWidth != (int)channelsWidth || pixelHeight != (int)channelsHeight)
    {
        printf("PGM image size does not match array size\n");
        return;
    }

    size_t nbRead = 0;

#if NB_BITS > 0 && NB_BITS != 8 && NB_BITS != 16 && NB_BITS != 32 && NB_BITS   \
                                                                     != 64
#if NB_BITS > 0 && NB_BITS < 8
    char inputsFixed[size];
#elif NB_BITS > 8 && NB_BITS < 16
    short inputsFixed[size];
#elif NB_BITS > 16 && NB_BITS < 32
    int inputsFixed[size];
#elif NB_BITS > 32 && NB_BITS < 64
    long long int inputsFixed[size];
#endif
    nbRead = fread(inputsFixed, sizeof(inputsFixed[0]), size, stimuli);

    for (unsigned int i = 0; i < size; ++i)
        data[i] = (Input_T)inputsFixed[i];
#else
    nbRead = read_mnist_input(data, sizeof(data[0]), size);
#endif
    if (nbRead != size) {
        printf("fread() number of read objects (%lu) different than"
                        " expected (%u) [data]\n", nbRead, size);
    }
//    for(int i = 0; i < nbRead; i+=16) {
//        printf("%X %X %X %X %X %X %X %X %X %X %X %X %X %X %X %X\n", 
//            data[i], data[i+1], data[i+2], data[i+3], 
//            data[i+4], data[i+5], data[i+6], data[i+7],
//            data[i+8], data[i+9], data[i+10], data[i+11], 
//            data[i+12], data[i+13], data[i+14], data[i+15]);
//    }

    nbRead = read_mnist_input(outputTargets, sizeof(outputTargets[0]), outputsSize);

    if (nbRead != outputsSize) {
        printf("fread() number of read objects (%lu) different than"
                        " expected (%u) [outputTargets]\n", nbRead, outputsSize);
    }

    if (!feof_mnist_input()) {
        printf("EOF not reached, File error !!\n");
    }
}
